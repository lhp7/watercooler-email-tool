"""Streamlit UI for sending Water Cooler CEO report emails."""

from __future__ import annotations

from io import BytesIO

import pandas as pd
import streamlit as st

from config import BRAND_COLORS, DEFAULT_PERIOD, MAX_RECIPIENTS, SENDER_FALLBACK_EMAIL, SENDER_FALLBACK_NAME
from email_sender import (
    build_match_table,
    extract_reports_from_zip,
    generate_eml_zip,
    get_smtp_settings,
    log_to_csv,
    read_recipients_csv,
    render_subject,
    render_text_body,
    send_emails_via_smtp,
    validate_recipients,
)


st.set_page_config(
    page_title="Water Cooler CEO Email Tool",
    page_icon="WC",
    layout="wide",
)


